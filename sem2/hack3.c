uint32_t bitcount(uint32_t v) {
	uint32_t c;
	c = (v & 0x55555555) + ((v >> 1) & 0x55555555);
	c = (c & 0x33333333) + ((c >> 2) & 0x33333333);
	c = (c & 0x0F0F0F0F) + ((c >> 4) & 0x0F0F0F0F);
	c = (c & 0x00FF00FF) + ((c >> 8) & 0x00FF00FF);
	c = (c & 0x0000FFFF) + ((c >> 16) & 0x0000FFFF);
	return c;
}


